// Generates src/diacritics_table.h from the `diacritic` npm package used by
// lumine/shared/select-list, so the native fold table matches the previous
// JS behaviour (Diacritics.clean) exactly.
//
// Usage: node tools/gen-diacritics-table.js
const path = require("path");
const fs = require("fs");

// The `diacritic` package lives in the lumine tree, not in fuzzy-native.
const Diacritics = require(path.resolve(
  __dirname,
  "../../lumine/node_modules/diacritic/diacritics.js",
));

const map = Diacritics.map; // char (code unit) -> base string

const entries = [];
for (const key of Object.keys(map)) {
  const cp = key.codePointAt(0);
  // Fold to lowercase so the folded form is also case-insensitive, matching
  // how the previous JS path cleaned then matched case-insensitively.
  const base = map[key].toLowerCase();
  entries.push({ cp, base });
}

// Sort by code point for a stable, binary-searchable table.
entries.sort((a, b) => a.cp - b.cp);

// De-dup (the diacritic map can list a code point twice; last wins in JS object
// key order, but here we just keep the first after sort — they're identical).
const seen = new Set();
const unique = [];
for (const e of entries) {
  if (seen.has(e.cp)) continue;
  seen.add(e.cp);
  unique.push(e);
}

function cppEscape(s) {
  return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

let out = "";
out += "// AUTO-GENERATED by tools/gen-diacritics-table.js — do not edit by hand.\n";
out += "// Maps a Unicode code point to its lowercase ASCII fold (may be >1 char).\n";
out += "#pragma once\n\n";
out += "#include <cstdint>\n\n";
out += "struct DiacriticFold {\n  uint32_t codepoint;\n  const char *base;\n};\n\n";
out += "// Sorted by codepoint (ascending) for binary search.\n";
out += `static const DiacriticFold kDiacriticFolds[] = {\n`;
for (const e of unique) {
  const hex = "0x" + e.cp.toString(16).toUpperCase().padStart(4, "0");
  out += `  {${hex}, "${cppEscape(e.base)}"},\n`;
}
out += "};\n\n";
out += `static const int kDiacriticFoldCount = ${unique.length};\n`;

const dest = path.resolve(__dirname, "../src/diacritics_table.h");
fs.writeFileSync(dest, out);
console.log(`Wrote ${unique.length} entries to ${dest}`);
